                    <p class="text-justify">The integration of computational biology and "omics" Sciences facilitate understanding the molecular mechanisms of diseases in an integrative way, including elucidation of complex networks of genes and proteins along with their regulatory mechanisms [4-7]. Genes do not act as individual units; they collaborate in overlapping pathways, the co-regulation of which is a hallmark for the disease pathogenesis.</p>
                    <p class="text-justify">Using systems approach we have already constructed the gene regulatory network models, comprised of high throughput genomic and PPI data for T2D and other inflammatory diseases such as obesity (OBS), hypertension (HT) and cardiovascular diseases (CVD). From pathway analysis, the involvement of p53 or Wnt signaling pathway shed new light to focus at the PPI networks (Jesmin et al., 2010). Network analysis revealed that T2D, HT, OBS are linked via two common gene regulatory cascades: (i) EP300/TP53/MYC/ CDKN1A/STAT3/CXCR4 and (ii) PPARG/PPARA/ IGF1R/AKT1/LEP/LEPR. Based on the investigations around the ‘hubs’ that provided much meaningful insights about the cross-talk within gene-disease networks, a hypothetical co-regulation disease mechanism model been proposed by our group. The results from this study revealed that the oxidative stress mediated regulation cascade is the common mechanistic link among the pathogenesis of T2D, HT and other inflammatory diseases such as OBS and cardiovascular diseases. The findings provide a novel comprehensive approach for understanding the pathogenesis of various co-associated chronic inflammatory diseases by combining the power of pathway analysis with gene regulatory network evaluation.</p>
